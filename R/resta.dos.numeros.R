resta.dos.numeros <- function(a, b) {
  #' @title Resta dos números
  #' @description Función que resta dos números.
  #' @param a minuendo.
  #' @param b sustraendo.
  #' @return La resta de los dos números.
  #' @export resta.dos.numeros
  #' @author Carlos D. Luján
  #' @examples
  #' resta.dos.numeros(2, 3)
  #'
  #' x <- 2
  #' y <- 3
  #' resta.dos.numeros(x, y)
  #'

  a - b
}